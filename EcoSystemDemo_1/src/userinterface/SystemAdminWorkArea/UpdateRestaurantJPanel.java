    private void btnAddResturantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddResturantActionPerformed
        // TODO add your handling code here:

        if(txtManagerName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter manager name!");
            return;
        }
        if(txtManagerUsername.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter manager username!");
            return;
        }
        for(Restaurant r: this.ecosystem.getRestaurantDirectory().getRestaurants()){
            if(r.getRestaurantAdmin().getUsername().equals(txtManagerUsername.getText())&&!currentRestaurant.getRestaurantAdmin().getUsername().equals(txtManagerUsername.getText())){
                JOptionPane.showMessageDialog(null, "Username exists! Enter different username");
                return;
            }
        }
        if(txtManagerPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter manager password!");
            return;
        }
        User user = ecosystem.getUserDirectory().createUser(txtManagerName.getText()); //employee name

        UserAccount userAccount = ecosystem.getUserAccountDirectory().createUserAccount(txtManagerUsername.getText(),txtManagerPassword.getText() , user, new AdminRole());

        //Restaurant res = ecosystem.getRestaurantDirectory().createRestaurant(null,txtResturantName.getText() , userAccount);               
        for(Restaurant restaurant:this.ecosystem.getRestaurantDirectory().getRestaurants()){
            if(currentRestaurant.equals(restaurant)){
                restaurant.setRestaurantAdmin(userAccount);
                break;
            }
        }
        dB4OUtil.storeSystem(ecosystem);
        JOptionPane.showMessageDialog(null,"Resturant created successfully!");
        txtManagerName.setText("");
        txtManagerPassword.setText("");
        txtManagerUsername.setText("");
        txtResturantName.setText("");
        return;

    }//GEN-LAST:event_btnAddResturantActionPerformed

